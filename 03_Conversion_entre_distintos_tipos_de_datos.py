### Conversion de tipos de datos ###

float = 2.5
string = str(float)
print(string)
print(type(string))

decimal_str = '25.90'
numero = float(decimal_str)
print(numero)
print(type(numero))


float = 2.5
numero = int(float)
print(numero)
print(type(numero))

string = "2"
numero2 = int(string)
print(numero2)
print(type(numero2))

suma = numero + numero2
print(suma)
print(type(suma))



