### Operadores de pertenencia ###

## Operador in ##
ciudades1 = ["Paris", "Barcelona", "Madrid"]
ciudades2 = "Barcelona"
print(ciudades2 in ciudades1)
ciudades2 = "Roma"
print(ciudades2 in ciudades1)

## Operador not in ##
ciudades1 = ["Paris", "Barcelona", "Madrid"]
ciudades2 = "Barcelona"
print(ciudades2 not in ciudades1)
ciudades2 = "Roma"
print(ciudades2 not in ciudades1)